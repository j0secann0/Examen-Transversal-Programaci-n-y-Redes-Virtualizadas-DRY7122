# Lista de integrantes del grupo
integrantes = [
    "José Martínez",
    "Ana Gómez",
    "Carlos Pérez",
    "María Fernández",
    "Luis Rodríguez"
]

# Función para imprimir la lista de integrantes
def imprimir_integrantes(integrantes):
    print("Integrantes del grupo:")
    for integrante in integrantes:
        print(integrante)

# Llamar a la función para imprimir la lista
imprimir_integrantes(integrantes)
